package jdk8.stream;

public class Customer {

	public static void main(String[] args) {
		
		
	}
	
	String name;
	int age;
	
	public Customer(String name, int age){
		this.name = name;
		this.age = age;
	}
	
	//getter and Setter
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public int getAge() {
		return age;
	}
	
	public void setAge(int age) {
		this.age = age;
	}

}
